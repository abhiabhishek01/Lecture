import java.util.Scanner;

public class ForLoop {
    public static void main(String[] args) {
       int j = 1;
        while ( j < 101){
            System.out.print(j+ " ");
            j++;
        }
        System.out.println();

        int i = 1 ;
        for (i = 1 ; i <= 100; i+= 2){
            System.out.print(i+" ");
        }
    }
}

